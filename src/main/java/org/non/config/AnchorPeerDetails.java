package org.non.config;

public class AnchorPeerDetails {
	private String host;
	private String port;
	
	public void setHost(String host){
		this.host=host;
	}
	
	public String getHost(){
		return this.host;
	}
	
	public void setPort(String port){
		this.port=port;
	}
	
	public String getPort(){
		return this.port;
	}
	

}
